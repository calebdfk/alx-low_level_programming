#include "3-calc.h"

/**
 * op_add - The sum of two numbers
 * @a: First number
 * @b: Second number
 * Return: The sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - The difference of two numbers
 * @a: First number
 * @b: Second number
 * Return: The difference of a and b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - The product of two numbers
 * @a: First number
 * @b: Second number
 * Return: The product of a and b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - The division of two numbers
 * @a: First number
 * @b: Second number
 *
 * Return: result of the division of the numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - The remainder of the division of two numbers.
 * @a: First number
 * @b: Second number
 *
 * Return: The remainder of the division of a by b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
